package com.cresset.asimjofaofficial.models;


public class CartAttributes {
    public String AttributeName;
    public String AttributeValue;
    public int Attributeprice;

    public String getAttributeName() {
        return AttributeName;
    }

    public void setAttributeName(String attributeName) {
        AttributeName = attributeName;
    }

    public String getAttributeValue() {
        return AttributeValue;
    }

    public void setAttributeValue(String attributeValue) {
        AttributeValue = attributeValue;
    }

    public int getAttributeprice() {
        return Attributeprice;
    }

    public void setAttributeprice(int attributeprice) {
        Attributeprice = attributeprice;
    }
}
